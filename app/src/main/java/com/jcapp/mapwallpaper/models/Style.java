package com.jcapp.mapwallpaper.models;

public class Style {

    private String name;
    private String nameNoText;
    private String position;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNameNoText() {
        return nameNoText;
    }

    public void setNameNoText(String nameNoText) {
        this.nameNoText = nameNoText;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

}